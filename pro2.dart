// Write a program to make addition, Subtraction, Multiplication and
// Division of Two Numbers
void main(List<String> args) {
  //addition
  int a = 10;
  int b = 20;
  print(a + b);

  //subtraction
  int c = 30;
  int d = 20;
  print(c - d);

  //multiplication
  int e = 20;
  int f = 10;
  print(e * f);

  //division
  int g = 20;
  int h = 10;
  print(g / h);
}
